#include <iostream>
using namespace std;
int main()
{
    int n = 4, k = 2;
    cout << ++n << endl;
     cout << n << endl;
     cout << n++ << endl;
     cout << n << endl;
     cout << -n << endl;
     cout << n << endl;
     cout << --n << endl;
     cout << n << endl;
     cout << n-- << endl;
     cout << n << endl;
     cout << n + k << endl;
     cout << n << endl;
     cout << k << endl;
     cout << n << k << endl;
     cout << n << endl;
     cout << " " << n << endl;
     cout << " n" << endl;
     cout << "\n"
          << endl;
     cout << " n * n = "; // CAREFUL!
     cout << n * n << endl;
     cout << 'n' << endl;
    return 0;
}